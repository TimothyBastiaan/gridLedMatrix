    export function LedMatrixWS2812b(leds: string, strip: neopixel.Strip, color: NeoPixelColors): void 
    {
        strip.setMatrixWidth(8);
        let setColor = neopixel.colors(color);
        let ledImage = imageConverter(leds);
        let width = ledImage.width();
        let height = ledImage.height();
        let heightL = height - 1;
        for (let i = 1; i <= width; i++) 
        {
            for (let j = 0; j < height; j++) 
            {
                if (i % 2 == 1) 
                {
                    if (ledImage.pixelBrightness(i - 1, heightL - j) == 255) 
                    {
                        strip.setMatrixColor(i - 1, j, neopixel.colors(color));
                    }
                }
                else 
                {
                    if (ledImage.pixelBrightness(i - 1, j) == 255) 
                    {
                        strip.setMatrixColor(i - 1, j, neopixel.colors(color));
                    }
                }
            }
        }
        basic.pause(50);
    }

